// operadores logico 

// 2 valores boleanos que quando verificados resultara em verdadeiro ou falso

let pao = true
let queijo = true

// AND &&
console.log(pao && queijo)

// or ||
console.log(pao || queijo)

// not !
console.log(!pao, !queijo) //muda o valor dela, se for true vira false e se for false vira true